package com.example.myapplication

//variable: there are two type of variable mutable and immutable
// we can reassign the values in mutable . var is used
// we cannot reassign the value in immutable variable . val is used

//declaring variable in kotlin
fun main() {
    var a: String = "anisha"
    var b: Int = 2
    var c: Float = 2.0F
    var d: Double = 4.5
    var e: Long=2434335L
    var f :Byte = 12
    var g: Short = -112


    println(a)
    println(b)
    println(c)
    println(d)
    println(e)
    println(f)
    println(g)
}
